def flatlist(listitem):
    finallist = []
    for lt in listitem:
        flattenedlist = []
        for listdata in lt:
            if isinstance(listdata, list) or isinstance(listdata, tuple):
                for singlelist in listdata:
                    flattenedlist.append(str(singlelist))
            else:
                flattenedlist.append(str(listdata))

        finallist.append(",".join(flattenedlist))
    
    return finallist


# Close and remove workbook before creating new.

def ensure_excel_closed(filename):
    from win32com.client.gencache import EnsureDispatch
    try:
        # Checks if any workbook is
        xl = EnsureDispatch('Excel.Application')
        # print("Active WB:", xl.ActiveWorkbook.Name)
        for wb in xl.Workbooks:
            if wb.Name == filename:
                # print("WB:", wb.Name)
                wb.Save()
                wb.Close()
        del xl
    except:
        # No excel workbook opened
        pass
